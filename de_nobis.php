        Investment Thesis: The market was clearly not happy with the court settlement, given that after more than five years of litigation, AMSC was only able to recoup $58M in damages, even though losses in annual revenue stemming from IP theft at the hands of Chinese wind giant Sinovel totaled more than $500M. However, while news regarding the wind segment is pushing down the stock price, the company's other operating segment is set to begin commercialization of a technology that could revamp the electric grid nationwide, allowing neighborhoods that lose power from a storm or terrorist attack to regain it much faster than legacy technology allows.</br>   
